<div class="center article">
	<h3>Why is biodiversity important for sustainable development?</h3>
	<p>
		<span class="darkGreen">Biodiversity is important for sustainable development in many ways, as it provides the critical foundation for sustainable development and human well-being:</span>
		Biodiversity is the critical foundation of the Earth’s life support system on which the welfare of current and future generations depends, thus supporting sustainable development. Humans are entirely dependent on ecosystems services for many basic needs. The foundations of many cultures, rural areas and cities depend on the services provided by ecosystems.
	</p>
	<h3>Why is sustainable development important for biodiversity?</h3>
	<p>
		<span class="darkGreen">A true sustainable development framework must not only acknowledge the role of biodiversity for development, it must also provide the enabling conditions for its conservation and sustainable use.</span> 
		To do this, the post-2015 framework needs to promote transformational change in economies and societies. This will require improved governance and institutions at multiple scales as well as behavioural change, and building human capabilities through access to education and health care.
	</p>
	<p>
		Economic growth contributes to poverty eradication, but must be pursued in a socially equitable way, with a reduced impact on biodiversity and ecosystems, if it is to address the multiple dimensions of human well-being in a sustainable manner, taking into account the needs of future generations.
		Growth will be essential in developing countries to eradicate poverty, and for the foreseeable future, it will be necessary in all countries to ensure full employment. Growth must therefore be decoupled from resource consumption and from negative impacts on biodiversity. Carbon neutral (or even carbon-negative) growth will be an important part of this transformation. It will almost certainly be necessary to look beyond growth-based prosperity, especially in high-income countries. Transformation to sustainable production and consumption patterns implies a more even sharing of resources among a greater number of people: greater equality. Waste of food and excessive levels of consumption of all resources will need to be reduced. More equal societies tend to have higher levels of well-being across all sectors of society. Equality may therefore be considered a means to development as well as an end in itself. Increased investment in education and health care, especially for women, will be essential to build human capabilities and facilitate demographic changes that, together with changes in per-capita consumption, will reduce pressures on biodiversity. Empowerment of women (including through literacy and employment) is a proven way to lower birth rates and thereby reduce the rate of population growth.
	</p>
	<p>
		Work to develop the post-2015 development agenda is ongoing within the United Nations system. These include discussions and consultations on the post-2015 development agenda and developing the Sustainable Development Goals.
	</p>

	<h5>Rio+20</h5>
	<p>
		The United Nations Conference on Sustainable Development, or Rio+20, held in in Rio de Janeiro, Brazil on 20-22 June 2012, resulted in a focused political outcome document which contains clear and practical measures for implementing sustainable development. The Conference of the Parties to the Convention on Biological Diversity, noting the intergovernmental process on sustainable development goals established in the outcome document of the Rio+20 Conference in the context of the United Nations development agenda beyond 2015, and stressing the importance of integrating biodiversity into these processes, noting the relevance of the Strategic Plan for Biodiversity 2011-2020 and its Aichi Biodiversity Targets, invited all Parties and stakeholders to integrate the three objectives of the Convention on Biological Diversity into sustainable development and poverty eradication programmes, plans, policies and priority actions, taking into account the outcomes of the Rio+20 Conference.
	</p>
	<p class="basic-list-p">
		Rio+20 reinstated the following as the overarching objectives and essential requirements for sustainable development: 
	</p>
	<ul class="basic-list">
		<li>poverty eradication</li>
		<li>changing unsustainable and promoting sustainable patterns of consumption and production </li>
		<li>protecting and managing the natural resource base of economic and social development </li>
		<li>promoting sustained, inclusive and equitable economic growth, social development and environment protection</li>
		<li>strengthening international cooperation.</li>
	</ul>
	<p>
		Poverty eradication, changing unsustainable and promoting sustainable patterns of consumption and production and protecting and managing the natural resource base of economic and social development are the overarching objectives of and essential requirements for sustainable development. Poverty eradication is the greatest global challenge facing the world today and an indispensable requirement for sustainable development. The Rio+20 outcome document, as a matter of urgency, reiterated the commitment to freeing humanity from poverty and hunger.
	</p>
	<p>
		People are at the centre of sustainable development. In this regard, Rio+20 promised to strive for a world that is just, equitable and inclusive, and committed to working together to promote sustained and inclusive economic growth, social development and environmental protection and thereby to benefit all. Poverty eradication can only be achieved if a number of structural factors such as livelihood, food security, health, education, employment, equality, inclusive growth, access to basic services and empowerment of individuals, can be addressed jointly in a sustainable manner, including the sustainable use of biodiversity and ecosystems. 
	</p>
	<p>
		In order to achieve a just balance among the economic, social and environmental needs of present and future generations, it is necessary to promote harmony with nature. The sustainable development goals acknowledge the natural and cultural diversity of the world, and confirm that all cultures and civilizations can contribute to sustainable development.
	</p>
	<p>
		The Sustainable Development Goals are accompanied by targets and will be further elaborated through indicators focused on measurable outcomes. Action-oriented, global in nature and universally applicable, the goals take into account different national realities, capacities and levels of development and respect national policies and priorities. They build on the foundation laid by the MDGs, seek to complete the unmet targets of the MDGs and respond to new challenges. The goals and targets integrate economic, social and environmental aspects and recognize their interlinkages in achieving sustainable development in all its dimensions.
	</p>
	<p>
		<span class="darkGreen">Biodiversity relates to all 17 sustainable development goals but there are two proposed goals that explicitly refer to it; goals 14 and 15.</span>
	</p>
